//User function Template for C++
class Solution
{
public:
    int trailingZeroes(int N)
    {
        if(N < 5){
            return 0;
        }
        return N/5 + trailingZeroes(N/5);
    }
};

//{ Driver Code Starts.
int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        int N;
        cin >> N;
        Solution ob;
        int ans  = ob.trailingZeroes(N);
        cout<<ans<<endl;
    }
    return 0;
}
